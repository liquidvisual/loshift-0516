;(function($) {
    'use strict';

    //==================================================
    // Variables
    //==================================================

    var $alertBar = $('.lv-alert-bar');

    //==================================================
    // Close Alert Bar
    //==================================================

    function closeAlertBar() {
        $alertBar.find('.lv-alert-bar-close-btn').click(function(){
            $alertBar.hide();
        });
    }

    $(document).ready(function() {
        closeAlertBar();
    });

}(jQuery));

